  CONSTRAINT `teacher_assignments_class_id_fkey` FOREIGN KEY (`class_id`) REFERENCES `classes`(`id`) ON UPDATE CASCADE,
  CONSTRAINT `teacher_assignments_section_id_fkey` FOREIGN KEY (`section_id`) REFERENCES `sections`(`id`) ON UPDATE CASCADE,
  CONSTRAINT `teacher_assignments_responsibility_id_fkey` FOREIGN KEY (`responsibility_id`) REFERENCES `teacher_responsibilities`(`id`) ON UPDATE CASCADE,
  CONSTRAINT `teacher_assignments_created_by_fkey` FOREIGN KEY (`created_by`) REFERENCES `admins`(`id`) ON DELETE SET NULL ON UPDATE CASCADE
) DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

