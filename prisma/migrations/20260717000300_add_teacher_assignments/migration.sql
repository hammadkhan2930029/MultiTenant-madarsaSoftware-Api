  `updated_at` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  UNIQUE INDEX `teacher_assign_scope_unique` (`tenant_id`, `branch_id`, `assignment_scope_key`),
