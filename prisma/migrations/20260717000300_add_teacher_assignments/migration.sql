CREATE TABLE IF NOT EXISTS `teacher_responsibilities` (
  `id` INTEGER NOT NULL AUTO_INCREMENT,
  `tenant_id` INTEGER NOT NULL,
  `branch_id` INTEGER NULL,
  `branch_scope_key` VARCHAR(50) NOT NULL DEFAULT 'tenant',
  `name` VARCHAR(150) NOT NULL,
  `status` VARCHAR(50) NOT NULL DEFAULT 'active',
  `created_by` INTEGER NULL,
  `created_at` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updated_at` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  UNIQUE INDEX `teacher_resp_scope_name_unique` (`tenant_id`, `branch_scope_key`, `name`),
  INDEX `teacher_resp_tenant_idx` (`tenant_id`),
  INDEX `teacher_resp_branch_idx` (`branch_id`),
  INDEX `teacher_resp_status_idx` (`status`),
  INDEX `teacher_resp_created_by_idx` (`created_by`),
  CONSTRAINT `teacher_responsibilities_tenant_id_fkey` FOREIGN KEY (`tenant_id`) REFERENCES `tenant`(`id`) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT `teacher_responsibilities_branch_id_fkey` FOREIGN KEY (`branch_id`) REFERENCES `branches`(`id`) ON DELETE SET NULL ON UPDATE CASCADE,
  CONSTRAINT `teacher_responsibilities_created_by_fkey` FOREIGN KEY (`created_by`) REFERENCES `admins`(`id`) ON DELETE SET NULL ON UPDATE CASCADE
) DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

